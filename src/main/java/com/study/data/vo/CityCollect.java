package com.study.data.vo;

public class CityCollect {

    private int cityCollectId;

    private String productId;

    private String cityId;

    private String pakageId;

    public int getCityCollectId() {
        return cityCollectId;
    }

    public void setCityCollectId(int cityCollectId) {
        this.cityCollectId = cityCollectId;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getCityId() {
        return cityId;
    }

    public void setCityId(String cityId) {
        this.cityId = cityId;
    }

    public String getPakageId() {
        return pakageId;
    }

    public void setPakageId(String pakageId) {
        this.pakageId = pakageId;
    }
}
